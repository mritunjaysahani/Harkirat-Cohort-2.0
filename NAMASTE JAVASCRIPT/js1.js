/**Episode 1: Execution Context & Call Stack

Execution Context = Environment where JS code runs.
It has 2 parts:

Memory/Variable Environment → stores variables & functions as key-value pairs.

Code Component/Thread of Execution → executes code line by line.

JS is synchronous and single-threaded → executes one thing at a time.

Call Stack → Keeps track of execution contexts.

Global Execution Context (GEC) created first.

Function Execution Context created when function is invoked.

Follows LIFO (Last In First Out).*/
